#  Python, memory management is handled automatically by a mechanism called the "Python memory manager". The memory manager is responsible for allocating memory for objects, tracking object references, and freeing memory when objects are no longer needed.
# 
# Python uses a technique called "reference counting" to keep track of object references. Each object in Python has a reference count, which is incremented whenever a new reference to the object is created, and decremented whenever a reference is deleted. When an object's reference count reaches zero, the memory used by the object is automatically freed.

